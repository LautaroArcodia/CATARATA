#define TOTAL_GLIFOS ((int)(sizeof FUENTE_HUD / sizeof FUENTE_HUD[0]))

const tGlifo *fuente_buscar_glifo(char c)
{
    for (int i = 0; i < TOTAL_GLIFOS; i++)
    {
        if (FUENTE_HUD[i].c == c)
            return &FUENTE_HUD[i];
    }
    return NULL;  /* carácter no encontrado en la fuente */
}
